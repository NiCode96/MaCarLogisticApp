    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
         
         String nombreTipo = txt_tipoRepuesto.getText().trim();
         TipoRepuestoController tipoRepuestoController = new TipoRepuestoController();
         
        try {
           
            if (!nombreTipo.isEmpty()&&!TipoProducto.verificarIngreso(nombreTipo)) {
                boolean evento = tipoRepuestoController.ingresoTipoProducto(nombreTipo);
           

                
                JOptionPane.showMessageDialog(this, "Tipo de producto ingresado correctamente");

            }
          
            
            else{
            
             JOptionPane.showMessageDialog(this, "Debe completar campos con datos no duplicados");
            
            }
           
          
           
            
        } catch (Exception e) {
           
            JOptionPane.showMessageDialog(this, "No se pudo conectar con base de datos problema en el paquete TipoPorductoVistaController ");
        }
        
        txt_tipoRepuesto.setText("");
        llenarCombo();
        llenarTabla();
    }//GEN-LAST:event_btn_agregarActionPerformed

