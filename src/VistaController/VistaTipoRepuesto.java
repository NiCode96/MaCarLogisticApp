    public void llenarTabla(){
    tabla_tipoRepuesto.setModel(model);
    model.setRowCount(0);
    
        try {
       
       TipoRepuestoController tipoRepuestoController = new TipoRepuestoController();
       ArrayList <TipoProducto> listaTipo = tipoRepuestoController.listaTodosTipos();
       
            for (TipoProducto tipoproductoindex : listaTipo) {
                Object[] objeto = new Object[2];
                objeto[0]= tipoproductoindex.getTipoNombre();
                objeto[1]= tipoproductoindex.getId_tipo();
                int estado = tipoproductoindex.getEstado_tipo();
                
                if (estado == 1) {
                    
                    model.addRow(objeto);
                }
                
                
            }
    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No es posible conectar con base de datos SQL (package.VistaController.vistaTipoRepuesto.err)");
        }
    
    }
    
    
    public void llenarCombo(){
    
        combo_todoTipo.removeAllItems();
        try {
       
       TipoRepuestoController tipoRepuestoController = new TipoRepuestoController();
       ArrayList <TipoProducto> listaTipo = tipoRepuestoController.listaTodosTipos();
       
            for (TipoProducto ProductoIndex : listaTipo) {
               
                int estado = ProductoIndex.getEstado_tipo();
                
                if (estado == 1) {
                    
                    combo_todoTipo.addItem(String.valueOf(ProductoIndex.getId_tipo()));
                }
                
                
            }
       
        } catch (Exception e) {
        }
               
       
        
    }
    
